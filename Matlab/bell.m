function bell
%小球在3根弹簧作用下的模型
u=0.0;m=0.2;g=9.8;
axis([0,10,0,8]);
hold on;box on;
f1=fill([1,1.2,1.2,1],[2,2,6,6],[0.8,0.2,0.8]);
f2=fill([8.8,9,9,8.8],[0.4,0.4,7.6,7.6],[0.8,0.2,0.8]);
p1=1.2+4i;p2=8.8+7i;p3=8.8+i;
k1=10;k2=10;k3=10;
pz=5+4i;
nk=[mod(1:16,2)-0.5]*0.5;
nk=[0,0,0,nk,0,0,0];
pd=pz+exp(i*[0:0.02:pi*2]);
fq=fill(real(pd),imag(pd),[0.7,0.8,0.9]);
n1=exp(i*[angle(p1-pz)+pi/2]);
pk1=linspace(pz,p1,22);
pk1=pk1+nk*n1;
hp1=plot(pk1);
n2=exp(i*[angle(p2-pz)+pi/2]);
pk2=linspace(pz,p2,22);
pk2=pk2+nk*n2;
hp2=plot(pk2);
n3=exp(i*[angle(p3-pz)+pi/2]);
pk3=linspace(pz,p3,22);
pk3=pk3+nk*n3;
hp3=plot(pk3);
l1=abs(p1-pz);l2=abs(p2-pz);l3=abs(p3-pz);
v=0;dt=0.01;
set(gcf,'doublebuffer','on');
tz=title('t=0');t=0;
%rp=[rand-0.5+i*(rand-0.5)*4];
rp=2+2i;
pz=pz+rp;pzz=pz;
f=1;
hg=plot(pz,'r.','markersize',2);
while abs(v)>1e-5|abs(f)>1e-4;
    f1=exp(i*[angle(p1-pz)])*(abs(p1-pz)-l1);
    f2=exp(i*[angle(p2-pz)])*(abs(p2-pz)-l2);
    f3=exp(i*[angle(p3-pz)])*(abs(p3-pz)-l3);
    f=f1+f2+f3;
    a=f/m;
    v=v+a*dt;
    pz=pz+v*dt;
    n1=exp(i*[angle(p1-pz)+pi/2]);
    pk1=linspace(pz,p1,22);
    pk1=pk1+nk*n1;
    set(hp1,'xdata',real(pk1),'ydata',imag(pk1));
    n2=exp(i*[angle(p2-pz)+pi/2]);
    pk2=linspace(pz,p2,22);
    pk2=pk2+nk*n2;
    set(hp2,'xdata',real(pk2),'ydata',imag(pk2));
    n3=exp(i*[angle(p3-pz)+pi/2]);
    pk3=linspace(pz,p3,22);
    pk3=pk3+nk*n3;
    set(hp3,'xdata',real(pk3),'ydata',imag(pk3));
    pd=pz+exp(i*[0:0.02:pi*2])*0.26;
    set(fq,'xdata',real(pd),'ydata',imag(pd));
    t=t+dt;
    if t<800;
        pzz=[pzz,pz];
        set(hg,'xdata',real(pzz),'ydata',imag(pzz));
    end
    set(tz,'string',['t=',num2str(t)]);
    pause(0.0000000000000002);
end